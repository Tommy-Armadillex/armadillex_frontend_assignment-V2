// Mock API response format for getTicketById
export function getMockTicketById(ticketId) {
  const baseTicket = MOCK_TICKETS_LIST.find((t) => t.ticket_id === ticketId)
  if (!baseTicket) return null

  const template = TICKET_DETAIL_TEMPLATES[baseTicket.type]

  const fullTicket = {
    ...baseTicket,
    questions_answers: template.questions_answers,
    deny_reason: template.deny_reason,
    auth_conditions: template.auth_conditions,
  }

  return {
    ticket_info: fullTicket,
  }
}

// Helper to generate dates
function daysAgo(days) {
  const date = new Date()
  date.setDate(date.getDate() - days)
  return date.toISOString()
}

function hoursAgo(hours) {
  const date = new Date()
  date.setHours(date.getHours() - hours)
  return date.toISOString()
}

